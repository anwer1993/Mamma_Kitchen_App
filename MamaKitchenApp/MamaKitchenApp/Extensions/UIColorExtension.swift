        return UIColor(red: 0.0, green: 138.0 / 255.0, blue: 71.0 / 255.0, alpha: 1.0)
    }
    @nonobjc class var black: UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
    @nonobjc class var black13: UIColor {
        return UIColor(white: 0.0, alpha: 0.13)
    }
    @nonobjc class var black9: UIColor {
        return UIColor(white: 0.0, alpha: 0.09)
    }
    @nonobjc class var black5: UIColor {
        return UIColor(white: 0.0, alpha: 0.05)
    }
    @nonobjc class var black16: UIColor {
        return UIColor(white: 0.0, alpha: 0.16)
    }
    @nonobjc class var black37: UIColor {
        return UIColor(white: 0.0, alpha: 0.37)
    }
    @nonobjc class var macaroonYellow: UIColor {
        return UIColor(red: 229.0 / 255.0, green: 209.0 / 255.0, blue: 92.0/255, alpha: 1.0)
    }
    @nonobjc class var macaroonYellow37: UIColor {
